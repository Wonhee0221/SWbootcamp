package ch05_반복문;

public class ex01_for문 {
	public static void main(String[] args) {
//		for문
//		반복 횟수를 알고 있을 때 적합함
//		for(초기화; 조건식; 증감식) 으로 구성되어 있음
//		만약 초기화 하는 경우에 둘 이상의 변수가 필요하면 콤마','로 구분하여 사용할 수 있음

		for (int i = 1; i <= 3; i++) { // 괄호{}안의 문장을 3번 반복
			System.out.println("Hello");
		}
	}

}
